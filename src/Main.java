public class Main {
    public static void main(String[] args) {
        int a=13;
        int b=4;
        System.out.println(a+b);//17
        System.out.println(a-b);//9
        System.out.println(a*b);//52
        System.out.println(a==b);//false
        System.out.println(a!=b);//true
        System.out.println(a>b);//true
        System.out.println(a<b);//false
        System.out.println(a>=b);//true
        System.out.println(a<=b);//false
        // 이 이후 부터는 정수형 비트 연산

    }
}